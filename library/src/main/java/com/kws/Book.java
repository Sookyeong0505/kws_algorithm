package com.kws;

public class Book {
    private int bookId;
    private String title;
    private String author;
    private String publisher;
    private int year;

    public Book(int bookId, String title, String author, String publisher, int year) {
        this.bookId = bookId;
        this.title = title;
        this.author = author;
        this.publisher = publisher;
        this.year = year;
    }




}
